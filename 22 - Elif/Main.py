# Elif
day = int(input("Masukan hari dalam angka (1-7) : "))

if day == 1:
    print("Hari ini adalah hari Senin!")
elif day == 2:
    print("Hari ini adalah hari Selasa!")
elif day == 3:
    print("Hari ini adalah hari Rabu!")
elif day == 4:
    print("Hari ini adalah hari Kamis!")
elif day == 5:
    print("Hari ini adalah hari Jum'at!")
elif day == 6:
    print("Hari ini adalah hari Sabtu!")
elif day == 7:
    print("Hari ini adalah hari Minggu!")
else:
    print("Sana bikin hari sendiri!!")

print("Terimkasih banyak!")
