# Operasi Aritmatika

"""
1. Tambah (+)
2. Kurang (-)
3. Kali (*)
4. Bagi (/)
5. Modulus (%)
6. Floor Division (//)
7. Ekponensial (**)
"""

panjang = 10
tinggi = 10
lebar = 10

# Tambah (+)
hasil_tambah = panjang + tinggi + lebar
print(panjang, "+", lebar, "+", lebar, "=", hasil_tambah)

# Kurang (-)
hasil_kurang = panjang - tinggi - lebar
print(panjang, "-", lebar, "-", lebar, "=", hasil_kurang)

# Kali (*)
kali = panjang * tinggi * lebar
print(panjang, "*", lebar, "*", lebar, "=", kali)

# Bagi (/)
hasil_bagi = panjang / tinggi / lebar
print(panjang, "/", lebar, "/", lebar, "=", hasil_bagi)

# Modulus (%)
hasil_modulus = panjang % 3
print(panjang, "%", 3, "=", hasil_modulus)

# Floor Division
hasil_floor_division = panjang // 3
print(panjang, "%%", 3, "=", hasil_floor_division)

# Eksponensial (**)
hasil_eksponensial = panjang ** 3
print(panjang, "**", 3, "=", hasil_eksponensial)


