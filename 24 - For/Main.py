# For Loop using List
numbers = [0, 1, 2, 3, 4]

print(f"""
For Loop using List
{numbers}
""")

for n in numbers:
    print(f"Number -> {n}")

print("End program 1")


# For Loop using Range
range_number = range(5)

print(f"""
For Loop using Range
{range_number}
""")

for n in range_number:
    print(f"Number -> {n}")

print("End program 2")


# For Loop using Custom start Range
range_number = range(1, 6)

print(f"""
For Loop using Custom start Range
{range_number}
""")
for n in range_number:
    print(f"Number -> {n}")

print("End program 3")


# For Loop using String
name = "Abdul Hafiz Ramadan"

print(f"""
For Loop using String
{name}
""")

for str in name:
    print(str)

print("End program 4")
